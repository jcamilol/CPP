#include <iostream>
#include <cstdlib>

using namespace std;

int main(){

    cout << "7 / 2 = " << 7/2 << endl;
    cout << "7.0 / 2 = " << 7.0/2 << endl;
    cout << "7 / 2.0 = " << 7/2.0 << endl;
    cout << "13 / 3 = " << 13/3 << endl;
    cout << "13.0 / 3 = " << 13.0/3 << endl;
    cout << "13 / 3.4 = " << 13/3.4 << endl;

    system("PAUSE");
    return 0;
}
